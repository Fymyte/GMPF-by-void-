#ifndef TYPES_REDEFINE_H
#define TYPES_REDEFINE_H

#include <stdint.h>

// sized exactly
// redifined for more comprehension in the code

typedef int8_t      Sint8; // char = 1 byte = 8 bits
typedef uint8_t     Uint8;
typedef int16_t     Sint16; // short = 2 bytes = 16 bits
typedef uint16_t    Uint16;
typedef int32_t     Sint32; // long = 4 bytes = 32 bits
typedef uint32_t    Uint32;
typedef int64_t     Sint64; // long long = 8 bytes = 64 bits
typedef uint64_t    Uint64;



#endif /* TYPES_REDEFINE_H */
